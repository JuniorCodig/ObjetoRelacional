    public void loadPedidos( ArrayList<Compra> list ) throws SQLException {
        
        DefaultTableModel listaPedidos = (DefaultTableModel) jTablePedido.getModel();
        
        list.forEach((c) -> {
            listaPedidos.addRow( new Object[] { c.getCodigo(), c.getCliente().getNome(), c.getArtigo().getDescricao(), c.getQuantidade(), c.getValor() });
        });
        
    }
    
