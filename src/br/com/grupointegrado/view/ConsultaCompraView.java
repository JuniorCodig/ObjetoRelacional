    private void jButtonVerPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerPedidoActionPerformed
        
        if ( jTablePedido.getSelectedRow() < 0 ) {
            JOptionPane.showMessageDialog(this, "Selecione um pedido");
            return;
        }
        
        DefaultTableModel table = (DefaultTableModel) jTablePedido.getModel();
        int codigoCompra = (int) table.getValueAt( jTablePedido.getSelectedRow(), 0);
        
        try {
            
            PedidoFaturadoView pedidoView = new PedidoFaturadoView( codigoCompra );
            pedidoView.setVisible(true);
            pedidoView.setLocationRelativeTo(this);
            
            pedidoView.addWindowListener( new java.awt.event.WindowAdapter() {
            
                public void windowClosed( WindowEvent e ) {
                
                }
            });
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ConsultaCompraView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButtonVerPedidoActionPerformed

