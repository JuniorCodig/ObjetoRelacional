    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        try {
            clienteDao = new ClienteDao();
            
            cliente = clienteDao.findOne( Integer.parseInt( jTextCodigo.getText() ) );
            
            clienteDao.delete(cliente);
            
            JOptionPane.showMessageDialog( this, "Cliente deletado." );
            
            jComboFidelidade.setSelectedIndex(-1);
            jTextNome.setText("");
            jTextCPF.setText("");
            jTextCodigo.setText("");
            jTextCodigo.setEnabled(true);
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AlteraExcluiClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jTextCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextCodigoFocusLost
        try {
            jTextCodigo.setEnabled(false);
            clienteDao = new ClienteDao();
            
            cliente = clienteDao.findOne( Integer.parseInt( jTextCodigo.getText() ) );
            
            jTextNome.setText( cliente.getNome() );
            jTextCPF.setText( String.valueOf( cliente.getCpf()) );
            
            this.fillComboFidelidade();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AlteraExcluiClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextCodigoFocusLost

