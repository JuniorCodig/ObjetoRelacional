    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        
        cliente.setCodigo( Integer.parseInt( jTextCodigo.getText() ) );
        cliente.setNome( jTextNome.getText() );
        cliente.setCpf( jTextCPF.getText() );
        
        fidelidade = (Fidelidade) jComboFidelidade.getSelectedItem();
        
        cliente.setFidelidade( fidelidade );
        
        try {
            clienteDao = new ClienteDao();
            
            clienteDao.update( cliente );
            
            JOptionPane.showMessageDialog(this, "Cliente alterado.");
        } catch ( SQLException | ClassNotFoundException e ) {
            JOptionPane.showMessageDialog( this, "Erro ao altera o cliente. Erro: " + e.getMessage() );
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

