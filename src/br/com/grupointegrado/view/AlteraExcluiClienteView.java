    public void fillComboFidelidade() throws ClassNotFoundException, SQLException {
        
        fidelidadeDao = new FidelidadeDao();
        list = new ArrayList<>();
        
        try {
            list = fidelidadeDao.findAll();

            for ( Object f : list ) {
                jComboFidelidade.addItem( f );
            }
            
        } catch ( SQLException e ) {
            JOptionPane.showMessageDialog( this, "Erro ao carregar fidelidades. Erro: " + e.getMessage() );
        }
     }

